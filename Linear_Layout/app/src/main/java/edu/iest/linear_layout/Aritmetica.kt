package edu.iest.linear_layout

class Aritmetica {
    fun plus(firstNum : Float, secondNum: Float) : Float {
        return firstNum + secondNum
    }

    fun minus(firstNum: Float, secondNum: Float) : Float {
        return firstNum - secondNum
    }

    fun mult(firstNum: Float, secondNum: Float) : Float {
        return firstNum * secondNum
    }

    fun div(firstNum: Float, secondNum: Float) : Float {
        return firstNum / secondNum
    }
}